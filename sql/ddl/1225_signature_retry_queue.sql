CREATE OR REPLACE TABLE DOCGEN.SIGNATURE_RETRY_QUEUE (
  RETRY_ID STRING PRIMARY KEY,
  TARGET_PROC STRING,
  PAYLOAD VARIANT,
  NEXT_RUN_AT TIMESTAMP_LTZ,
  ATTEMPTS INT DEFAULT 0,
  CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP()
);

