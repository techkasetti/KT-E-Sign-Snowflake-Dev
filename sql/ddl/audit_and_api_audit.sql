Purpose: API_AUDIT and ActivationAudit alignment to capture admin operations and make them queryable for reconciliation. @74 @189

USE DATABASE AI_FEATURE_HUB;
USE SCHEMA DOCGEN;

CREATE OR REPLACE TABLE DOCGEN.API_AUDIT (
  API_AUDIT_ID STRING PRIMARY KEY,
  ACCOUNT_ID STRING,
  API_NAME STRING,
  REQUEST_PAYLOAD VARIANT,
  RESPONSE_PAYLOAD VARIANT,
  STATUS STRING,
  CALLED_BY STRING,
  CALLED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE OR REPLACE TABLE DOCGEN.ACTIVATION_AUDIT_CONSOLIDATED AS
SELECT * FROM DOCGEN.ACTIVATION_AUDIT;

