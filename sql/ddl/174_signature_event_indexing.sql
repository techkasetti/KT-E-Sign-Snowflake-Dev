-- Materialized view for fast lookup of recent signature events for dashboards and searches. @31 @3149
CREATE OR REPLACE MATERIALIZED VIEW DOCGEN.MV_RECENT_SIGNATURE_EVENTS AS
SELECT EVENT_ID, REQUEST_ID, SIGNER_ID, EVENT_TYPE, EVENT_TS
FROM DOCGEN.SIGNATURE_EVENTS
WHERE EVENT_TS >= DATEADD(hour, -24, CURRENT_TIMESTAMP());

