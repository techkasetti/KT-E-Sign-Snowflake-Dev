-- Signature requests domain table for tracking the lifecycle of a signature request as required by the E-Sign gap analysis. @7 @14
USE DATABASE AI_FEATURE_HUB;
USE SCHEMA DOCGEN;
CREATE OR REPLACE TABLE DOCGEN.SIGNATURE_REQUESTS (
  REQUEST_ID STRING PRIMARY KEY,
  DOCUMENT_ID STRING,
  ACCOUNT_ID STRING,
  CREATED_BY STRING,
  CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
  STATUS STRING,
  CURRENT_SIGNER_INDEX NUMBER,
  METADATA VARIANT
);

