CREATE OR REPLACE PROCEDURE DOCGEN.ENQUEUE_SMS(template_id STRING, phone_number STRING, context VARIANT, next_attempt_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SMS_QUEUE (QUEUE_ID, TEMPLATE_ID, PHONE_NUMBER, CONTEXT, NEXT_ATTEMPT_AT) VALUES (UUID_STRING(), :template_id, :phone_number, :context, :next_attempt_at);
$$

