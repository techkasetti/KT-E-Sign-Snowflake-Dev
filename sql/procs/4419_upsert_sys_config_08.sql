CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_SYS_CONFIG_08(key STRING, value VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.SYS_CONFIG_08 t USING (SELECT :key AS k, :value AS v) s ON t.KEY = s.k WHEN MATCHED THEN UPDATE SET VALUE = s.v, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (KEY, VALUE) VALUES (s.k, s.v);
$$

