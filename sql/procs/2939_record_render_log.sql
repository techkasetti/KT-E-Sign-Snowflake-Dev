CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_RENDER_LOG(queue_id STRING, status STRING, started_at TIMESTAMP_LTZ, ended_at TIMESTAMP_LTZ, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.RENDER_LOGS (LOG_ID, QUEUE_ID, STATUS, STARTED_AT, ENDED_AT, DETAILS) VALUES (UUID_STRING(), :queue_id, :status, :started_at, :ended_at, :details);
$$

