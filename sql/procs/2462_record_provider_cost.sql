CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_PROVIDER_COST(provider_id STRING, cost_currency STRING, cost_amount NUMBER)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_COSTS (COST_ID, PROVIDER_ID, COST_CURRENCY, COST_AMOUNT) VALUES (UUID_STRING(), :provider_id, :cost_currency, :cost_amount);
$$

