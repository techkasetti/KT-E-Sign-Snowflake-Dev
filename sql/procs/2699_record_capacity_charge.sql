CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_CAPACITY_CHARGE(account_id STRING, amount NUMBER, currency STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.CAPACITY_BILLING_CHARGES (CHARGE_ID, ACCOUNT_ID, AMOUNT, CURRENCY, META) VALUES (UUID_STRING(), :account_id, :amount, :currency, :meta);
$$

