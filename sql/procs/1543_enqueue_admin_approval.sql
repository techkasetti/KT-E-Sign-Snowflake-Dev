CREATE OR REPLACE PROCEDURE DOCGEN.ENQUEUE_ADMIN_APPROVAL(entity_ref STRING, reason STRING, requested_by STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.ADMIN_APPROVAL_QUEUE (APPROVAL_ID, ENTITY_REF, REASON, REQUESTED_BY) VALUES (UUID_STRING(), :entity_ref, :reason, :requested_by);
$$;

