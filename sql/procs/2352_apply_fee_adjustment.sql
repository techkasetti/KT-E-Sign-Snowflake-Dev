CREATE OR REPLACE PROCEDURE DOCGEN.APPLY_FEE_ADJUSTMENT(line_item_id STRING, adjusted_by STRING, adjustment_amount NUMBER, currency STRING, reason STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FEE_ADJUSTMENTS (ADJ_ID, BILLING_LINE_ITEM_ID, ADJUSTED_BY, ADJUSTMENT_AMOUNT, CURRENCY, REASON)
VALUES (UUID_STRING(), :line_item_id, :adjusted_by, :adjustment_amount, :currency, :reason);
$$

