CREATE OR REPLACE PROCEDURE DOCGEN.REPORT_THREAT_INTEL(signer_id STRING, source STRING, risk_level STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SIGNER_THREAT_INTEL (INTEL_ID, SIGNER_ID, SOURCE, RISK_LEVEL, DETAILS) VALUES (UUID_STRING(), :signer_id, :source, :risk_level, :details);
$$;

