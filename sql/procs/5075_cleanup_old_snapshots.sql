USE DATABASE AI_FEATURE_HUB;
USE SCHEMA DOCGEN;
CREATE OR REPLACE PROCEDURE DOCGEN.CLEANUP_OLD_SNAPSHOTS(days_threshold INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
DELETE FROM DOCGEN.INDEX_SNAPSHOTS WHERE CREATED_AT < DATEADD(day, -days_threshold, CURRENT_TIMESTAMP());
$$;

