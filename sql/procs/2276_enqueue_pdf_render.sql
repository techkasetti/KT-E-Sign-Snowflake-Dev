RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PDF_RENDER_QUEUE (QUEUE_ID, DOCUMENT_ID, TEMPLATE_ID, NEXT_ATTEMPT_AT) VALUES (UUID_STRING(), :document_id, :template_id, :next_attempt_at);
$$;

