CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_CONSENT(signer_id STRING, consent_type STRING, consent_given BOOLEAN)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.CONSENT_LOGS (CONSENT_ID, SIGNER_ID, CONSENT_TYPE, CONSENT_GIVEN)
VALUES (UUID_STRING(), :signer_id, :consent_type, :consent_given);
$$;

