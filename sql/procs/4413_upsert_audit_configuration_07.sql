CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_AUDIT_CONFIGURATION_07(config_id STRING, name STRING, config VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.AUDIT_CONFIGURATIONS_07 t USING (SELECT :config_id AS cid, :name AS nm, :config AS c) s ON t.CONFIG_ID = s.cid WHEN MATCHED THEN UPDATE SET NAME = s.nm, CONFIG = s.c, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (CONFIG_ID, NAME, CONFIG) VALUES (s.cid, s.nm, s.c);
$$

