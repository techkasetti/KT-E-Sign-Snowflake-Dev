CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_API_CLIENT(client_name STRING, client_secret_hash STRING, owner_account STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.API_CLIENTS (CLIENT_ID, CLIENT_NAME, CLIENT_SECRET_HASH, OWNER_ACCOUNT) VALUES (UUID_STRING(), :client_name, :client_secret_hash, :owner_account);
$$;

