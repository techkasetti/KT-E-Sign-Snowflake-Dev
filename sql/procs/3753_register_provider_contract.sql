CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_PROVIDER_CONTRACT(contract_id STRING, provider_name STRING, terms VARIANT, effective_at TIMESTAMP_LTZ, expires_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_CONTRACTS (CONTRACT_ID, PROVIDER_NAME, TERMS, EFFECTIVE_AT, EXPIRES_AT)
VALUES (:contract_id, :provider_name, :terms, :effective_at, :expires_at);
$$

