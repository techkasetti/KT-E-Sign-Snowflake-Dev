CREATE OR REPLACE VIEW DOCGEN.V_OPERATOR_MONITOR AS
SELECT ir.INCIDENT_ID, ir.SEVERITY, ir.STATUS, sa.ACTION_TS FROM DOCGEN.INCIDENT_REPORTS ir LEFT JOIN DOCGEN.OPERATOR_ACTION_LOG sa ON sa.TARGET = ir.INCIDENT_ID ORDER BY ir.CREATED_AT DESC;

