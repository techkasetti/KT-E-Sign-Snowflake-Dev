CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_VERIFICATION_RESULT_05(q_id STRING, status STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.VERIFICATION_RESULTS_05 (RESULT_ID, Q_ID, STATUS, DETAILS) VALUES (UUID_STRING(), :q_id, :status, :details);
UPDATE DOCGEN.VERIFICATION_QUEUE_05 SET STATUS = :status WHERE Q_ID = :q_id;
$$

