CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_ACCESS(template_id STRING, accessed_by STRING, action STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_ACCESS_LOGS (LOG_ID, TEMPLATE_ID, ACCESSED_BY, ACTION, META) VALUES (UUID_STRING(), :template_id, :accessed_by, :action, :meta);
$$

