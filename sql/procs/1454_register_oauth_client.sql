CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_OAUTH_CLIENT(account_id STRING, client_name STRING, redirect_uris ARRAY)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.OAUTH_CLIENTS (CLIENT_ID, ACCOUNT_ID, CLIENT_NAME, REDIRECT_URIS) VALUES (UUID_STRING(), :account_id, :client_name, :redirect_uris);
$$;

