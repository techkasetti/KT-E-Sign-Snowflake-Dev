CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_ADMIN_ACTION(admin_user STRING, action STRING, target_ref STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.ADMIN_ACTIONS_LOG (LOG_ID, ADMIN_USER, ACTION, TARGET_REF, DETAILS) VALUES (UUID_STRING(), :admin_user, :action, :target_ref, :details);
$$

