CREATE OR REPLACE PROCEDURE DOCGEN.CREATE_API_KEY(account_id STRING, key_hash STRING, scope VARIANT, expires_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.API_KEYS (KEY_ID, ACCOUNT_ID, KEY_HASH, SCOPE, EXPIRES_AT) VALUES (UUID_STRING(), :account_id, :key_hash, :scope, :expires_at);
$$

