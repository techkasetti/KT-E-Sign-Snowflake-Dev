CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_VALIDATION_RULE_07(rule_id STRING, name STRING, expression STRING, error_msg STRING, enabled BOOLEAN)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.VALIDATION_RULES_07 t USING (SELECT :rule_id AS rid, :name AS nm, :expression AS expr, :error_msg AS em, :enabled AS en) s ON t.RULE_ID = s.rid WHEN MATCHED THEN UPDATE SET NAME = s.nm, EXPRESSION = s.expr, ERROR_MSG = s.em, ENABLED = s.en WHEN NOT MATCHED THEN INSERT (RULE_ID, NAME, EXPRESSION, ERROR_MSG, ENABLED) VALUES (s.rid, s.nm, s.expr, s.em, s.en);
$$

