CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_REVIEW(queue_id STRING, reviewer STRING, outcome STRING, notes CLOB)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_REVIEW_LOGS (LOG_ID, QUEUE_ID, REVIEWER, OUTCOME, NOTES) VALUES (UUID_STRING(), :queue_id, :reviewer, :outcome, :notes);
UPDATE DOCGEN.TEMPLATE_REVIEW_QUEUE SET STATUS = :outcome WHERE QUEUE_ID = :queue_id;
$$
