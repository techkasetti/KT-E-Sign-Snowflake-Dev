CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_THRESHOLD_NOTIFICATION(threshold_id STRING, account_id STRING, metric_name STRING, value NUMBER)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.THRESHOLD_NOTIFICATIONS (NOTIF_ID, THRESHOLD_ID, ACCOUNT_ID, METRIC_NAME, VALUE) VALUES (UUID_STRING(), :threshold_id, :account_id, :metric_name, :value);
$$

