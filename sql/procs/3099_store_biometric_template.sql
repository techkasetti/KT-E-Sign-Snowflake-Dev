CREATE OR REPLACE PROCEDURE DOCGEN.STORE_BIOMETRIC_TEMPLATE(subject_ref STRING, template_hash STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.BIOMETRIC_TEMPLATES (TEMPLATE_ID, SUBJECT_REF, TEMPLATE_HASH, META) VALUES (UUID_STRING(), :subject_ref, :template_hash, :meta);
$$
