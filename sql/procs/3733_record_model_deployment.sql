CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_MODEL_DEPLOYMENT(deploy_id STRING, model_id STRING, environment STRING, status STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.MODEL_DEPLOYMENTS (DEPLOY_ID, MODEL_ID, ENVIRONMENT, STATUS, META)
VALUES (:deploy_id, :model_id, :environment, :status, :meta);
$$

