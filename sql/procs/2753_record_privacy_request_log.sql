CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_PRIVACY_REQUEST_LOG(pr_id STRING, action STRING, by_user STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PRIVACY_REQUEST_LOGS (LOG_ID, PR_ID, ACTION, BY_USER, DETAILS) VALUES (UUID_STRING(), :pr_id, :action, :by_user, :details);
$$

