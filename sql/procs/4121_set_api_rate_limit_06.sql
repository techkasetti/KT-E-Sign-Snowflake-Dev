CREATE OR REPLACE PROCEDURE DOCGEN.SET_API_RATE_LIMIT_06(limit_id STRING, client_id STRING, limits VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.API_RATE_LIMITS_06 t USING (SELECT :limit_id AS lid, :client_id AS cid, :limits AS l) s ON t.LIMIT_ID = s.lid WHEN MATCHED THEN UPDATE SET CLIENT_ID = s.cid, LIMITS = s.l WHEN NOT MATCHED THEN INSERT (LIMIT_ID, CLIENT_ID, LIMITS) VALUES (s.lid, s.cid, s.l);
$$

