CREATE OR REPLACE PROCEDURE DOCGEN.COMPUTE_QUERY_ANALYTICS(period_start TIMESTAMP_LTZ, period_end TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.QUERY_ANALYTICS (ANALYTIC_ID, PERIOD_START, PERIOD_END, TOP_QUERIES)
VALUES (UUID_STRING(), :period_start, :period_end, PARSE_JSON('{}'));
$$

