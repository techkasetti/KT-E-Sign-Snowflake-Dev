CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_REVIEW_RESULT(template_id STRING, reviewer STRING, result STRING, comments STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_REVIEW_RESULTS (RESULT_ID, TEMPLATE_ID, REVIEWER, RESULT, COMMENTS) VALUES (UUID_STRING(), :template_id, :reviewer, :result, :comments);
$$
