CREATE OR REPLACE PROCEDURE DOCGEN.APPLY_LOCALE_OVERRIDE(account_id STRING, locale STRING, overrides VARIANT, eff_from TIMESTAMP_LTZ, eff_to TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.LOCALE_OVERRIDES (OVERRIDE_ID, ACCOUNT_ID, LOCALE, OVERRIDES, EFFECTIVE_FROM, EFFECTIVE_TO) VALUES (UUID_STRING(), :account_id, :locale, :overrides, :eff_from, :eff_to);
$$;

