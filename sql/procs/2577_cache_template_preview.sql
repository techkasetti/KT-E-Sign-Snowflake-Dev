CREATE OR REPLACE PROCEDURE DOCGEN.CACHE_TEMPLATE_PREVIEW(template_id STRING, payload VARIANT, preview_url STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_PREVIEW_CACHE (CACHE_ID, TEMPLATE_ID, PAYLOAD, PREVIEW_URL) VALUES (UUID_STRING(), :template_id, :payload, :preview_url);
$$

