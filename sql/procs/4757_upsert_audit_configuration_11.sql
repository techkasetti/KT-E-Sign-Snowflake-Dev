CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_AUDIT_CONFIGURATION_11(config_id STRING, name STRING, rules VARIANT, enabled BOOLEAN)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.AUDIT_CONFIGURATIONS_11 t USING (SELECT :config_id AS cid, :name AS nm, :rules AS r, :enabled AS e) s ON t.CONFIG_ID = s.cid WHEN MATCHED THEN UPDATE SET NAME = s.nm, RULES = s.r, ENABLED = s.e, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (CONFIG_ID, NAME, RULES, ENABLED) VALUES (s.cid, s.nm, s.r, s.e);
$$
