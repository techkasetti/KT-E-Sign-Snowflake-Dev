CREATE OR REPLACE PROCEDURE DOCGEN.ENQUEUE_ASYNC_CALLBACK(target_url STRING, payload VARIANT, next_run_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.ASYNC_CALLBACKS (CALLBACK_ID, TARGET_URL, PAYLOAD, NEXT_RUN_AT) VALUES (UUID_STRING(), :target_url, :payload, :next_run_at);
$$;

