CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_ACCESS_POLICY_CHANGE(policy_id STRING, changed_by STRING, changes VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.ACCESS_POLICY_CHANGES (CHANGE_ID, POLICY_ID, CHANGED_BY, CHANGES) VALUES (UUID_STRING(), :policy_id, :changed_by, :changes);
$$

