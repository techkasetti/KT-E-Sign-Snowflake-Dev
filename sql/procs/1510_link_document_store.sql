CREATE OR REPLACE PROCEDURE DOCGEN.LINK_DOCUMENT_STORE(request_id STRING, path STRING, checksum STRING, size NUMBER)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SIGNATURE_DOCUMENT_STORE (DOCSTORE_ID, REQUEST_ID, STORAGE_PATH, CHECKSUM, SIZE_BYTES) VALUES (UUID_STRING(), :request_id, :path, :checksum, :size);
$$;

