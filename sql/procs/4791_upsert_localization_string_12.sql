CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_LOCALIZATION_STRING_12(key STRING, locale STRING, value STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.LOCALIZATION_STRINGS_12 t USING (SELECT :key AS k, :locale AS l, :value AS v) s ON t.KEY = s.k AND t.LOCALE = s.l WHEN MATCHED THEN UPDATE SET VALUE = s.v, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (KEY, LOCALE, VALUE) VALUES (s.k, s.l, s.v);
$$

