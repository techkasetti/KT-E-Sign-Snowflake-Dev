CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_ADMIN_AUDIT_INDEX_08(index_id STRING, name STRING, definition VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.ADMIN_AUDIT_INDEXES_08 t USING (SELECT :index_id AS iid, :name AS nm, :definition AS def) s ON t.INDEX_ID = s.iid WHEN MATCHED THEN UPDATE SET NAME = s.nm, DEFINITION = s.def, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (INDEX_ID, NAME, DEFINITION) VALUES (s.iid, s.nm, s.def);
$$

