CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_MEDIA_ACCESS(media_id STRING, accessed_by STRING, action STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.MEDIA_ACCESS_LOGS (LOG_ID, MEDIA_ID, ACCESSED_BY, ACTION, DETAILS) VALUES (UUID_STRING(), :media_id, :accessed_by, :action, :details);
$$

