CREATE OR REPLACE PROCEDURE DOCGEN.UPDATE_ADMIN_AUDIT_CONFIG_08(config_id STRING, name STRING, settings VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.ADMIN_AUDIT_CONFIGS_08 t USING (SELECT :config_id AS cid, :name AS nm, :settings AS s) s ON t.CONFIG_ID = s.cid WHEN MATCHED THEN UPDATE SET NAME = s.nm, SETTINGS = s.s, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (CONFIG_ID, NAME, SETTINGS) VALUES (s.cid, s.nm, s.s);
$$

