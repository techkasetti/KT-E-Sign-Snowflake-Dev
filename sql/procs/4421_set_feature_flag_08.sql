CREATE OR REPLACE PROCEDURE DOCGEN.SET_FEATURE_FLAG_08(flag_id STRING, name STRING, enabled BOOLEAN, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.FEATURE_FLAGS_08 t USING (SELECT :flag_id AS fid, :name AS n, :enabled AS e, :meta AS m) s ON t.FLAG_ID = s.fid WHEN MATCHED THEN UPDATE SET NAME = s.n, ENABLED = s.e, META = s.m, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (FLAG_ID, NAME, ENABLED, META) VALUES (s.fid, s.n, s.e, s.m);
$$

