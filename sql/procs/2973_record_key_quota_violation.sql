CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_KEY_QUOTA_VIOLATION(key_ref_id STRING, window_start TIMESTAMP_LTZ, ops INT, quota INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.KEY_QUOTA_VIOLATIONS (VIOL_ID, KEY_REF_ID, WINDOW_START, OPS, QUOTA) VALUES (UUID_STRING(), :key_ref_id, :window_start, :ops, :quota);
$$

