CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_REFUND_PAYMENT(refund_id STRING, payment_method STRING, amount NUMBER, currency STRING, paid_at TIMESTAMP_LTZ, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.REFUND_PAYMENTS (PAYMENT_ID, REFUND_ID, PAYMENT_METHOD, AMOUNT, CURRENCY, PAID_AT, META)
VALUES (UUID_STRING(), :refund_id, :payment_method, :amount, :currency, :paid_at, :meta);
$$

