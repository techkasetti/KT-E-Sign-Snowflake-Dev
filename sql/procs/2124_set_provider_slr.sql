CREATE OR REPLACE PROCEDURE DOCGEN.SET_PROVIDER_SLR(provider_id STRING, slr_definition VARIANT, eff_from TIMESTAMP_LTZ, eff_to TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_SLRS (SLR_ID, PROVIDER_ID, SLR_DEFINITION, EFFECTIVE_FROM, EFFECTIVE_TO) VALUES (UUID_STRING(), :provider_id, :slr_definition, :eff_from, :eff_to);
$$

