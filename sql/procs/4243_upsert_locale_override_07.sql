CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_LOCALE_OVERRIDE_07(override_id STRING, account_id STRING, key STRING, value VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.LOCALE_OVERRIDES_07 t USING (SELECT :override_id AS oid, :account_id AS aid, :key AS k, :value AS v) s ON t.OVERRIDE_ID = s.oid WHEN MATCHED THEN UPDATE SET KEY = s.k, VALUE = s.v, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (OVERRIDE_ID, ACCOUNT_ID, KEY, VALUE) VALUES (s.oid, s.aid, s.k, s.v);
$$

