CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_DISPATCH_LOG(request_id STRING, provider_id STRING, outcome STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_DISPATCH_LOG (DISPATCH_ID, REQUEST_ID, PROVIDER_ID, OUTCOME, DETAILS) VALUES (UUID_STRING(), :request_id, :provider_id, :outcome, :details);
$$;

