CREATE OR REPLACE PROCEDURE DOCGEN.LOG_TEMPLATE_ACCESS(template_id STRING, user_id STRING, access_type STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_ACCESS_LOGS (ACCESS_ID, TEMPLATE_ID, USER_ID, ACCESS_TYPE) VALUES (UUID_STRING(), :template_id, :user_id, :access_type);
$$;

