CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_PROVIDER_ENDPOINT_CHANGE(provider_id STRING, url STRING, changed_by STRING, notes VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_ENDPOINTS_HISTORY (PID, PROVIDER_ID, URL, CHANGED_BY, NOTES) VALUES (UUID_STRING(), :provider_id, :url, :changed_by, :notes);
$$;

