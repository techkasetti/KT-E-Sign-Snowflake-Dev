CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_SLA_EVALUATION(sla_id STRING, period_start TIMESTAMP_LTZ, period_end TIMESTAMP_LTZ, metrics VARIANT, result VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SLA_EVALUATIONS (EVAL_ID, SLA_ID, PERIOD_START, PERIOD_END, METRICS, RESULT) VALUES (UUID_STRING(), :sla_id, :period_start, :period_end, :metrics, :result);
$$

