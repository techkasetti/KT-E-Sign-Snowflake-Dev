CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_AUDIT_EXPORT_RESULT_02(export_id STRING, location STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.AUDIT_EXPORT_RESULTS_02 (RESULT_ID, EXPORT_ID, LOCATION) VALUES (UUID_STRING(), :export_id, :location);
UPDATE DOCGEN.AUDIT_EXPORTS_02 SET STATUS = 'COMPLETED' WHERE EXPORT_ID = :export_id;
$$

