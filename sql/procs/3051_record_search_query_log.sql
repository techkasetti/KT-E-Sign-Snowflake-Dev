CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_SEARCH_QUERY_LOG(query_text STRING, user_ref STRING, results_count INT, runtime_ms INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SEARCH_QUERY_LOGS (LOG_ID, QUERY_TEXT, USER_REF, RESULTS_COUNT, RUNTIME_MS) VALUES (UUID_STRING(), :query_text, :user_ref, :results_count, :runtime_ms);
$$

