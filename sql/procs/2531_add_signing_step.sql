CREATE OR REPLACE PROCEDURE DOCGEN.ADD_SIGNING_STEP(workflow_id STRING, seq INT, step_type STRING, step_config VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SIGNING_STEPS (STEP_ID, WORKFLOW_ID, SEQ, STEP_TYPE, STEP_CONFIG) VALUES (UUID_STRING(), :workflow_id, :seq, :step_type, :step_config);
$$

