CREATE OR REPLACE PROCEDURE DOCGEN.SET_PROVIDER_TIMEOUT_01(provider_config_id STRING, timeout_seconds INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_TIMEOUTS_01 (TIMEOUT_ID, PROVIDER_CONFIG_ID, TIMEOUT_SECONDS) VALUES (UUID_STRING(), :provider_config_id, :timeout_seconds)
ON DUPLICATE KEY UPDATE TIMEOUT_SECONDS = :timeout_seconds;
$$

