CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_RESOLUTION_RUN(template_id STRING, incident_id STRING, status STRING, ended_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.RESOLUTION_RUNS (RUN_ID, TEMPLATE_ID, INCIDENT_ID, STATUS, ENDED_AT) VALUES (UUID_STRING(), :template_id, :incident_id, :status, :ended_at);
$$

