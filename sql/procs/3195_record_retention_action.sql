CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_RETENTION_ACTION(policy_id STRING, target_ref STRING, status STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.RETENTION_ACTIONS (ACTION_ID, POLICY_ID, TARGET_REF, EXECUTED_AT, STATUS) VALUES (UUID_STRING(), :policy_id, :target_ref, CURRENT_TIMESTAMP(), :status);
$$

