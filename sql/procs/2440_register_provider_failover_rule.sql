CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_PROVIDER_FAILOVER_RULE(source_provider STRING, target_providers ARRAY, conditions VARIANT, priority INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_FAILOVER_RULES (RULE_ID, SOURCE_PROVIDER, TARGET_PROVIDERS, CONDITIONS, PRIORITY) VALUES (UUID_STRING(), :source_provider, :target_providers, :conditions, :priority);
$$

