CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_REDACTION_RESULT_04(result_id STRING, task_id STRING, status STRING, output VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.REDACTION_RESULTS_04 (RESULT_ID, TASK_ID, STATUS, OUTPUT) VALUES (:result_id, :task_id, :status, :output);
UPDATE DOCGEN.REDACTION_TASKS_04 SET STATUS = :status WHERE TASK_ID = :task_id;
$$

