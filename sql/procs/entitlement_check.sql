USE DATABASE AI_FEATURE_HUB; USE SCHEMA DOCGEN;
CREATE OR REPLACE PROCEDURE DOCGEN.ENTITLEMENT_CHECK(account_id STRING, feature_name STRING)
RETURNS BOOLEAN
LANGUAGE SQL
AS
$$
SELECT IFF(EXISTS (SELECT 1 FROM DOCGEN.ACCOUNT_FEATURE_PRICING WHERE ACCOUNT_ID = :account_id AND FEATURE_NAME = :feature_name), TRUE, FALSE);
$$;
