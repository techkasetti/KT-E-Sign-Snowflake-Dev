CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_SIGNER_PROFILE_10(signer_id STRING, account_id STRING, name STRING, email STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.SIGNER_PROFILES_10 t USING (SELECT :signer_id AS sid, :account_id AS aid, :name AS nm, :email AS em, :meta AS m) s ON t.SIGNER_ID = s.sid WHEN MATCHED THEN UPDATE SET ACCOUNT_ID = s.aid, NAME = s.nm, EMAIL = s.em, META = s.m WHEN NOT MATCHED THEN INSERT (SIGNER_ID, ACCOUNT_ID, NAME, EMAIL, META) VALUES (s.sid, s.aid, s.nm, s.em, s.m);
$$

