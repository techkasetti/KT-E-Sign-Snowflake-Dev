CREATE OR REPLACE PROCEDURE DOCGEN.REVOKE_CONSENT(consent_id STRING, revoked_by STRING, reason STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.CONSENT_REVOCATIONS (REVOC_ID, CONSENT_ID, REVOKED_BY, REASON) VALUES (UUID_STRING(), :consent_id, :revoked_by, :reason);
UPDATE DOCGEN.USER_CONSENT_RECORDS SET META = OBJECT_INSERT(META, 'revoked', TRUE) WHERE CONSENT_ID = :consent_id;
$$

