CREATE OR REPLACE PROCEDURE DOCGEN.ADD_DOCUMENT_VERSION(document_id STRING, version_number INT, content_ref STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.DOCUMENT_VERSIONS (VERSION_ID, DOCUMENT_ID, VERSION_NUMBER, CONTENT_REF) VALUES (UUID_STRING(), :document_id, :version_number, :content_ref);
$$

