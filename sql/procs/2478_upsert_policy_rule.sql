CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_POLICY_RULE(name STRING, condition VARIANT, actions VARIANT, enabled BOOLEAN)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.POLICY_ENGINE_RULES t USING (SELECT :name AS nm, :condition AS cond, :actions AS acts, :enabled AS en) s ON t.NAME = s.nm WHEN MATCHED THEN UPDATE SET CONDITION = s.cond, ACTIONS = s.acts, ENABLED = s.en, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (RULE_ID, NAME, CONDITION, ACTIONS, ENABLED) VALUES (UUID_STRING(), s.nm, s.cond, s.acts, s.en);
$$;

