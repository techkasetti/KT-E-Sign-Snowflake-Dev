CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_NOTIFICATION_PREFERENCES_12(pref_id STRING, account_id STRING, channels ARRAY)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.NOTIFICATION_PREFERENCES_12 t USING (SELECT :pref_id AS pid, :account_id AS aid, :channels AS ch) s ON t.PREF_ID = s.pid WHEN MATCHED THEN UPDATE SET ACCOUNT_ID = s.aid, CHANNELS = s.ch, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (PREF_ID, ACCOUNT_ID, CHANNELS) VALUES (s.pid, s.aid, s.ch);
$$

