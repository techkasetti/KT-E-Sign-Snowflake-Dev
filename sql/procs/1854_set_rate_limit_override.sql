CREATE OR REPLACE PROCEDURE DOCGEN.SET_RATE_LIMIT_OVERRIDE(account_id STRING, endpoint STRING, limit_value INT, expires_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.RATE_LIMIT_OVERRIDES (OVERRIDE_ID, ACCOUNT_ID, ENDPOINT, LIMIT_VALUE, EXPIRES_AT)
VALUES (UUID_STRING(), :account_id, :endpoint, :limit_value, :expires_at);
$$;

