CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TRANSFORM_STEP(bundle_id STRING, step_name STRING, status STRING, output VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.EVIDENCE_TRANSFORMS (TRANSFORM_ID, BUNDLE_ID, STEP_NAME, STATUS, OUTPUT) VALUES (UUID_STRING(), :bundle_id, :step_name, :status, :output);
$$

