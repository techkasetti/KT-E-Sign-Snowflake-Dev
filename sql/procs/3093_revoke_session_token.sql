CREATE OR REPLACE PROCEDURE DOCGEN.REVOKE_SESSION_TOKEN(session_id STRING, revoked_by STRING, reason STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SESSION_REVOCATIONS (REVOC_ID, SESSION_ID, REVOKED_BY, REASON) VALUES (UUID_STRING(), :session_id, :revoked_by, :reason);
UPDATE DOCGEN.SESSION_TOKENS SET EXPIRES_AT = CURRENT_TIMESTAMP() WHERE SESSION_ID = :session_id;
$$
