CREATE OR REPLACE PROCEDURE DOCGEN.ADD_DYNAMIC_ROUTING_RULE(name STRING, conditions VARIANT, targets VARIANT, priority INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.DYNAMIC_ROUTING_RULES (RULE_ID, NAME, CONDITIONS, TARGETS, PRIORITY) VALUES (UUID_STRING(), :name, :conditions, :targets, :priority);
$$

