CREATE OR REPLACE PROCEDURE DOCGEN.CREATE_MODEL_CANARY(version_id STRING, percent_rollout FLOAT, metrics VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.MODEL_CANARY_CONFIG (CANARY_ID, VERSION_ID, PERCENT_ROLLOUT, METRICS) VALUES (UUID_STRING(), :version_id, :percent_rollout, :metrics);
$$;

