CREATE OR REPLACE PROCEDURE DOCGEN.GRANT_EXPORT_ACCESS(export_id STRING, granted_to STRING, permissions VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.EXPORT_ACCESS_CONTROLS (AC_ID, EXPORT_ID, GRANTED_TO, PERMISSIONS) VALUES (UUID_STRING(), :export_id, :granted_to, :permissions);
$$

