CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_FEATURE_USAGE(account_id STRING, feature_name STRING, count NUMBER)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FEATURE_USAGE_LEDGER (LEDGER_ID, ACCOUNT_ID, FEATURE_NAME, USAGE_COUNT)
VALUES (UUID_STRING(), :account_id, :feature_name, :count);
$$;

