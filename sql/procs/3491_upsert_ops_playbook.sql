CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_OPS_PLAYBOOK(playbook_id STRING, name STRING, steps VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.OPS_PLAYBOOKS t USING (SELECT :playbook_id AS pid, :name AS nm, :steps AS st) s ON t.PLAYBOOK_ID = s.pid WHEN MATCHED THEN UPDATE SET NAME = s.nm, STEPS = s.st, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (PLAYBOOK_ID, NAME, STEPS) VALUES (s.pid, s.nm, s.st);
$$

