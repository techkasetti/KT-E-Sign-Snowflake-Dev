CREATE OR REPLACE PROCEDURE DOCGEN.ADD_PROVIDER_CONTRACT(provider_id STRING, contract_text CLOB, start_at TIMESTAMP_LTZ, end_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_CONTRACTS (CONTRACT_ID, PROVIDER_ID, CONTRACT_TEXT, START_AT, END_AT) VALUES (UUID_STRING(), :provider_id, :contract_text, :start_at, :end_at);
$$;

