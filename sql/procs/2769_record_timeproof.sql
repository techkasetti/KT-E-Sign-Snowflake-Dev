CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TIMEPROOF(bundle_id STRING, timestamp_proof STRING, anchor_type STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TIMEPROOF_INDEX (TP_ID, BUNDLE_ID, TIMESTAMP_PROOF, ANCHOR_TYPE) VALUES (UUID_STRING(), :bundle_id, :timestamp_proof, :anchor_type);
$$

