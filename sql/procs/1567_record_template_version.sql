CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_VERSION(template_id STRING, version_number INT, body CLOB, updated_by STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_VERSIONS_HISTORY (TVH_ID, TEMPLATE_ID, VERSION_NUMBER, BODY, UPDATED_BY) VALUES (UUID_STRING(), :template_id, :version_number, :body, :updated_by);
$$;

