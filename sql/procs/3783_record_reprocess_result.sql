CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_REPROCESS_RESULT(queue_id STRING, status STRING, output VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.REPROCESS_RESULTS (RESULT_ID, QUEUE_ID, STATUS, OUTPUT) VALUES (UUID_STRING(), :queue_id, :status, :output);
UPDATE DOCGEN.REPROCESS_QUEUE SET STATUS = :status WHERE QUEUE_ID = :queue_id;
$$

