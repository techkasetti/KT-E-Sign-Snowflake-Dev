CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_USAGE(template_id STRING, account_id STRING, context VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_USAGE_HISTORY (HISTORY_ID, TEMPLATE_ID, ACCOUNT_ID, CONTEXT) VALUES (UUID_STRING(), :template_id, :account_id, :context);
$$

