CREATE OR REPLACE PROCEDURE DOCGEN.CREATE_SERVICE_TOKEN(service_id STRING, token_hash STRING, expires_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SERVICE_TOKENS (TOKEN_ID, SERVICE_ID, TOKEN_HASH, EXPIRES_AT) VALUES (UUID_STRING(), :service_id, :token_hash, :expires_at);
$$

