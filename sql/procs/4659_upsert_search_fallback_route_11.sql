CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_SEARCH_FALLBACK_ROUTE_11(route_id STRING, name STRING, primary_method STRING, fallback_methods ARRAY, enabled BOOLEAN)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.SEARCH_FALLBACK_ROUTES_11 t USING (SELECT :route_id AS rid, :name AS nm, :primary_method AS pm, :fallback_methods AS fm, :enabled AS e) s ON t.ROUTE_ID = s.rid WHEN MATCHED THEN UPDATE SET NAME = s.nm, PRIMARY_METHOD = s.pm, FALLBACK_METHODS = s.fm, ENABLED = s.e WHEN NOT MATCHED THEN INSERT (ROUTE_ID, NAME, PRIMARY_METHOD, FALLBACK_METHODS, ENABLED) VALUES (s.rid, s.nm, s.pm, s.fm, s.e);
$$

