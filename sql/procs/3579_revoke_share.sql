CREATE OR REPLACE PROCEDURE DOCGEN.REVOKE_SHARE(share_id STRING, revoked_by STRING, reason STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SHARE_REVOCATIONS (REVOC_ID, SHARE_ID, REVOKED_BY, REASON) VALUES (UUID_STRING(), :share_id, :revoked_by, :reason);
UPDATE DOCGEN.DOCUMENT_SHARES SET PERMISSIONS = NULL WHERE SHARE_ID = :share_id;
$$

