CREATE OR REPLACE PROCEDURE DOCGEN.UPDATE_NOTIFICATION_PREFERENCES_10(pref_id STRING, signer_id STRING, channels ARRAY)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.NOTIFICATION_PREFERENCES_10 t USING (SELECT :pref_id AS pid, :signer_id AS sid, :channels AS ch) s ON t.PREF_ID = s.pid WHEN MATCHED THEN UPDATE SET SIGNER_ID = s.sid, CHANNELS = s.ch, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (PREF_ID, SIGNER_ID, CHANNELS) VALUES (s.pid, s.sid, s.ch);
$$

