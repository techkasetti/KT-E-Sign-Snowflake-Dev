CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_FLAG_EVAL_LOG(flag_name STRING, evaluated_for STRING, result BOOLEAN, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FLAG_EVAL_LOGS (LOG_ID, FLAG_NAME, EVALUATED_FOR, RESULT, DETAILS) VALUES (UUID_STRING(), :flag_name, :evaluated_for, :result, :details);
$$;

