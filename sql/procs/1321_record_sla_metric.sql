-- Record an SLA metric point for operational dashboards and alerts. @1 @31
CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_SLA_METRIC(name STRING, value VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.SLA_METRICS (METRIC_ID, METRIC_NAME, METRIC_VALUE) VALUES (UUID_STRING(), :name, :value);
$$;

