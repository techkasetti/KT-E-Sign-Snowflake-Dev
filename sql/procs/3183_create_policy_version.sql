CREATE OR REPLACE PROCEDURE DOCGEN.CREATE_POLICY_VERSION(rule_id STRING, version_number INT, payload VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.POLICY_VERSIONS (VERSION_ID, RULE_ID, VERSION_NUMBER, DEFINED_AT, PAYLOAD) VALUES (UUID_STRING(), :rule_id, :version_number, CURRENT_TIMESTAMP(), :payload);
$$

