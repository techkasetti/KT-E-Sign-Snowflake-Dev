CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_BIOMETRIC_ATTEMPT(signer_id STRING, template_id STRING, score FLOAT, matched BOOLEAN, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.BIOMETRIC_MATCH_ATTEMPTS (ATTEMPT_ID, SIGNER_ID, TEMPLATE_ID, SCORE, MATCHED, META) VALUES (UUID_STRING(), :signer_id, :template_id, :score, :matched, :meta);
$$

