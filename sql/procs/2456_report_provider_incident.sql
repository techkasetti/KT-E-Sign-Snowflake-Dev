CREATE OR REPLACE PROCEDURE DOCGEN.REPORT_PROVIDER_INCIDENT(provider_id STRING, severity STRING, description STRING, meta VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_INCIDENTS (INCIDENT_ID, PROVIDER_ID, SEVERITY, DESCRIPTION, META) VALUES (UUID_STRING(), :provider_id, :severity, :description, :meta);
$$

