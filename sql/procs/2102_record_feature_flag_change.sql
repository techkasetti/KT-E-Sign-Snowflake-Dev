CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_FEATURE_FLAG_CHANGE(flag_name STRING, old_value BOOLEAN, new_value BOOLEAN, changed_by STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FEATURE_FLAG_HISTORY (FFH_ID, FLAG_NAME, OLD_VALUE, NEW_VALUE, CHANGED_BY) VALUES (UUID_STRING(), :flag_name, :old_value, :new_value, :changed_by);
$$

