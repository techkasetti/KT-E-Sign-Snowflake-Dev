CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_TEMPLATE_STATE_CHANGE(template_id STRING, state STRING, changed_by STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.TEMPLATE_LIFECYCLE (LIFECYCLE_ID, TEMPLATE_ID, STATE, CHANGED_BY) VALUES (UUID_STRING(), :template_id, :state, :changed_by);
$$
