CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_PROVIDER_CREDENTIAL_AUDIT(cred_id STRING, action STRING, performed_by STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_CREDENTIAL_AUDIT (AUDIT_ID, CRED_ID, ACTION, PERFORMED_BY, DETAILS) VALUES (UUID_STRING(), :cred_id, :action, :performed_by, :details);
$$

