CREATE OR REPLACE PROCEDURE DOCGEN.SCHEDULE_OCSP_POLL(cert_id STRING, next_poll_at TIMESTAMP_LTZ, poll_interval_seconds INT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.OCSP_POLL_SCHEDULE (SCHEDULE_ID, CERT_ID, NEXT_POLL_AT, POLL_INTERVAL_SECONDS) VALUES (UUID_STRING(), :cert_id, :next_poll_at, :poll_interval_seconds);
$$

