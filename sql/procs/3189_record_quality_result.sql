CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_QUALITY_RESULT(check_id STRING, target_ref STRING, result VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.QUALITY_RESULTS (RESULT_ID, CHECK_ID, TARGET_REF, RESULT, RAN_AT) VALUES (UUID_STRING(), :check_id, :target_ref, :result, CURRENT_TIMESTAMP());
$$

