CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_PROVIDER_COMPLIANCE_CERT(provider_id STRING, cert_ref STRING, issued_at TIMESTAMP_LTZ, expires_at TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_COMPLIANCE_CERTS (PC_ID, PROVIDER_ID, CERT_REF, ISSUED_AT, EXPIRES_AT) VALUES (UUID_STRING(), :provider_id, :cert_ref, :issued_at, :expires_at);
$$

