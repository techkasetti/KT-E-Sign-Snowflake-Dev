CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_UI_CONFIG_13(config_id STRING, account_id STRING, config VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.UI_CONFIG_13 t USING (SELECT :config_id AS cid, :account_id AS aid, :config AS cfg) s ON t.CONFIG_ID = s.cid WHEN MATCHED THEN UPDATE SET ACCOUNT_ID = s.aid, CONFIG = s.cfg, UPDATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (CONFIG_ID, ACCOUNT_ID, CONFIG) VALUES (s.cid, s.aid, s.cfg);
$$

