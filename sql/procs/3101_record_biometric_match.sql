CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_BIOMETRIC_MATCH(template_id STRING, score FLOAT, result STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.BIOMETRIC_MATCH_LOGS (LOG_ID, TEMPLATE_ID, SCORE, RESULT, DETAILS) VALUES (UUID_STRING(), :template_id, :score, :result, :details);
$$
