CREATE OR REPLACE PROCEDURE DOCGEN.INCREMENT_TEMPLATE_USAGE(template_id STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.TEMPLATE_USAGE_STATS t USING (SELECT :template_id AS tid) s ON t.TEMPLATE_ID = s.tid WHEN MATCHED THEN UPDATE SET USAGE_COUNT = COALESCE(USAGE_COUNT,0)+1, LAST_USED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (STAT_ID, TEMPLATE_ID, USAGE_COUNT, LAST_USED_AT) VALUES (UUID_STRING(), s.tid, 1, CURRENT_TIMESTAMP());
$$
