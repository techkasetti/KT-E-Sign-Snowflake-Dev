CREATE OR REPLACE PROCEDURE DOCGEN.GENERATE_ACCOUNT_SIGNATURE_SUMMARY(account_id STRING, period_start TIMESTAMP_LTZ, period_end TIMESTAMP_LTZ)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.ACCOUNT_SIGNATURE_SUMMARIES (SUMMARY_ID, ACCOUNT_ID, PERIOD_START, PERIOD_END, METRICS) VALUES (UUID_STRING(), :account_id, :period_start, :period_end, PARSE_JSON('{}'));
$$

