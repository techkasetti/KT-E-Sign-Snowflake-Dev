CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_SLA_BENCHMARK_07(bench_id STRING, name STRING, metric STRING, threshold VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.SLA_BENCHMARKS_07 t USING (SELECT :bench_id AS bid, :name AS nm, :metric AS m, :threshold AS th) s ON t.BENCH_ID = s.bid WHEN MATCHED THEN UPDATE SET NAME = s.nm, METRIC = s.m, THRESHOLD = s.th WHEN NOT MATCHED THEN INSERT (BENCH_ID, NAME, METRIC, THRESHOLD) VALUES (s.bid, s.nm, s.m, s.th);
$$

