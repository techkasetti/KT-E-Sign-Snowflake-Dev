CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_BILLING_CACHE_11(cache_id STRING, account_id STRING, preview VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.BILLING_CACHE_11 t USING (SELECT :cache_id AS cid, :account_id AS aid, :preview AS pv) s ON t.CACHE_ID = s.cid WHEN MATCHED THEN UPDATE SET ACCOUNT_ID = s.aid, PREVIEW = s.pv, CREATED_AT = CURRENT_TIMESTAMP() WHEN NOT MATCHED THEN INSERT (CACHE_ID, ACCOUNT_ID, PREVIEW) VALUES (s.cid, s.aid, s.pv);
$$
