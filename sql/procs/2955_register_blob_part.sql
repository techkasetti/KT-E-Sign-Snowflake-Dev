CREATE OR REPLACE PROCEDURE DOCGEN.REGISTER_BLOB_PART(bundle_id STRING, part_number INT, location STRING, size_bytes NUMBER)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.BLOB_PART_INDEX (PART_ID, BUNDLE_ID, PART_NUMBER, LOCATION, SIZE_BYTES) VALUES (UUID_STRING(), :bundle_id, :part_number, :location, :size_bytes);
$$

