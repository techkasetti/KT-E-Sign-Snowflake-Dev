CREATE OR REPLACE PROCEDURE DOCGEN.UPSERT_PUSH_TEMPLATE(template_id STRING, name STRING, title STRING, body STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
MERGE INTO DOCGEN.PUSH_TEMPLATES t
USING (SELECT :template_id AS tid, :name AS nm, :title AS ttl, :body AS bd) s
ON t.TEMPLATE_ID = s.tid
WHEN MATCHED THEN UPDATE SET NAME = s.nm, TITLE = s.ttl, BODY = s.bd
WHEN NOT MATCHED THEN INSERT (TEMPLATE_ID, NAME, TITLE, BODY) VALUES (s.tid, s.nm, s.ttl, s.bd);
$$

