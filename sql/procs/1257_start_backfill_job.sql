-- Kick off a backfill job and record metadata for operator visibility. @31 @24 @52
CREATE OR REPLACE PROCEDURE DOCGEN.START_BACKFILL_JOB(job_type STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$ INSERT INTO DOCGEN.SIGNATURE_BACKFILL_JOBS (JOB_ID, JOB_TYPE, STATUS, STARTED_AT) VALUES (UUID_STRING(), :job_type, 'RUNNING', CURRENT_TIMESTAMP());
$$;

