CREATE OR REPLACE PROCEDURE DOCGEN.ADD_PROVIDER_REGION(provider_id STRING, region STRING, endpoint VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_REGIONS (REGION_ID, PROVIDER_ID, REGION, ENDPOINT) VALUES (UUID_STRING(), :provider_id, :region, :endpoint);
$$; 
