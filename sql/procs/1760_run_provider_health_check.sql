CREATE OR REPLACE PROCEDURE DOCGEN.RUN_PROVIDER_HEALTH_CHECK(provider_id STRING, check_type STRING, status STRING, details VARIANT)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.PROVIDER_HEALTH_CHECKS (CHECK_ID, PROVIDER_ID, CHECK_TYPE, STATUS, DETAILS) VALUES (UUID_STRING(), :provider_id, :check_type, :status, :details);
$$;

