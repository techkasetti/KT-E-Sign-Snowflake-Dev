CREATE OR REPLACE PROCEDURE DOCGEN.RECORD_FLAG_CHANGE(flag_name STRING, old_val VARIANT, new_val VARIANT, changed_by STRING)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FLAG_AUDIT (AUDIT_ID, FLAG_NAME, OLD_VALUE, NEW_VALUE, CHANGED_BY) VALUES (UUID_STRING(), :flag_name, :old_val, :new_val, :changed_by);
$$;

