CREATE OR REPLACE PROCEDURE DOCGEN.CREATE_FINANCIAL_RECONCILIATION(account_id STRING, period_start DATE, period_end DATE)
RETURNS VARIANT
LANGUAGE SQL
AS
$$
INSERT INTO DOCGEN.FINANCIAL_RECONCILIATIONS (RECON_ID, ACCOUNT_ID, PERIOD_START, PERIOD_END) VALUES (UUID_STRING(), :account_id, :period_start, :period_end);
$$

