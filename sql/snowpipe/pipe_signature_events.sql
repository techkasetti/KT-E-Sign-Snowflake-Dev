Purpose: Snowpipe and FILE FORMAT to ingest signature event JSONL payloads into raw stage and stream for downstream Snowpark processing, following ingestion patterns in your docs @9 @41.  
-- pipe_signature_events.sql
USE DATABASE AI_FEATURE_HUB;
USE SCHEMA DOCGEN;
CREATE OR REPLACE FILE FORMAT DOCGEN.FF_JSONL TYPE = 'JSON' STRIP_OUTER_ARRAY = TRUE;
CREATE OR REPLACE STAGE DOCGEN.SIGNATURE_EVENTS_STAGE FILE_FORMAT = DOCGEN.FF_JSONL;
CREATE OR REPLACE PIPE DOCGEN.P_PIPE_SIGNATURE_EVENTS AUTO_INGEST = TRUE AS COPY INTO DOCGEN.SIGNATURE_EVENTS_RAW FROM @DOCGEN.SIGNATURE_EVENTS_STAGE FILE_FORMAT = (FORMAT_NAME = 'DOCGEN.FF_JSONL');  

