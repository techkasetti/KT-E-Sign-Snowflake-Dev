-- View for pending compliance reviews dashboard. @31 @24 @52
CREATE OR REPLACE VIEW DOCGEN.V_PENDING_REVIEWS AS
SELECT r.REVIEW_ID, b.REQUEST_ID, r.REVIEWER, r.RESULT FROM DOCGEN.COMPLIANCE_REVIEWS r LEFT JOIN DOCGEN.SIGNATURE_EVIDENCE_BUNDLE b ON r.BUNDLE_ID = b.BUNDLE_ID WHERE r.RESULT IS NULL;

